# Integer
x_int = 10
print("Type of x_int:", type(x_int))

# Float
y_float = 3.14
print("Type of y_float:", type(y_float))

# String
z_str = "Hello, World!"
print("Type of z_str:", type(z_str))

# Boolean
is_true = True
is_false = False
print("Type of is_true:", type(is_true))
print("Type of is_false:", type(is_false))

# List
list_example = [1, 2, 3, 4, 5]
print("Type of list_example:", type(list_example))

# Tuple
tuple_example = (1, 2, 3, 4, 5)
print("Type of tuple_example:", type(tuple_example))

# Set
set_example = {1, 2, 3, 4, 5}
print("Type of set_example:", type(set_example))

# Dictionary
dict_example = {'a': 1, 'b': 2, 'c': 3}
print("Type of dict_example:", type(dict_example))

# Complex Number
complex_number = 3 + 4j
print("Type of complex_number:", type(complex_number))

# NoneType
none_value = None
print("Type of none_value:", type(none_value))


# This program will print the type of each variable using the `type()` function.